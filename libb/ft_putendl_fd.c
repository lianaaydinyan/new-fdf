#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	if (fd > 0 || s[i])
	{
		ft_putstr_fd(s, fd);
		write(fd, "\n", 1);
	}
}
